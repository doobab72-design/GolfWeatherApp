package com.golfweather.util

/**
 * 위도/경도 → 기상청 중기예보 지역 코드 변환
 *
 * 기상청 중기육상예보(getMidLandFcst) 지역코드:
 *   11B00000: 서울·인천·경기
 *   11D10000: 강원 영서
 *   11D20000: 강원 영동
 *   11C20000: 충북
 *   11C10000: 충남·대전·세종
 *   11F20000: 경북·대구
 *   11F10000: 경남·부산·울산
 *   11H10000: 전북·광주
 *   11H20000: 전남
 *   11G00000: 제주
 *
 * 기상청 중기기온예보(getMidTa) 대표 지점 코드:
 *   11B10001: 서울, 11B20201: 인천, 11B20601: 수원
 *   11D10301: 춘천, 11D10401: 원주, 11D20501: 강릉, 11D20601: 동해
 *   11C20401: 청주, 11C10301: 대전, 11C10101: 홍성
 *   11F20201: 안동, 11F20401: 대구, 11F20501: 포항
 *   11H10501: 전주, 11H10201: 광주, 11H20101: 목포, 11H20301: 여수
 *   11F10201: 창원, 11H20201: 순천, 11F10501: 부산, 11F10401: 울산
 *   11G00201: 제주, 11G00401: 서귀포
 *
 * 개선 사항:
 *  - 직사각형 겹침 문제 해결: 우선순위 기반 순차 매핑 (더 구체적인 지역을 먼저 배치)
 *  - 한반도 전체를 커버하는 폴백 로직 추가
 *  - 영동/영서 경계(태백산맥 기준)를 경도 128.5°로 근사 적용
 *  - 제주/서귀포 분리: 위도 33.35° 기준으로 북쪽=제주, 남쪽=서귀포
 *    (두 지역 모두 동일한 중기육상예보 코드(11G00000)를 사용하나 기온 지점 코드가 다름)
 *  - 경기북부/남부 겹침 해소: 경기 남부(수원) vs 서울 대표지점 분리
 */
object MidTermRegionMapper {

    data class Region(
        val name: String,
        val latMin: Double,
        val latMax: Double,
        val lonMin: Double,
        val lonMax: Double,
        val landRegId: String,
        val taRegId: String,
        val taCity: String
    )

    /**
     * 우선순위 순서 (위쪽이 더 높은 우선순위).
     * 경계가 겹치는 영역은 더 구체적인 지역이 먼저 매칭되도록 순서를 배치.
     *
     * 변경 이력:
     *  - 제주/서귀포 위도 기준으로 분리 (이전: 경도 범위 겹침으로 서귀포가 절대 매칭 안 됨)
     *    제주시 중심: 33.50°N, 서귀포시 중심: 33.25°N → 위도 33.35° 기준 분할
     *  - 경기남부 수원 대표지점 추가 (위도 36.9~37.4° 남부 경기)
     */
    private val regions = listOf(
        // 제주 북부 (제주시 방향, 위도 33.35° 이상)
        Region("제주",         33.35, 33.6, 126.1, 127.0, "11G00000", "11G00201", "제주"),

        // 서귀포 (제주 남부, 위도 33.35° 미만)
        Region("서귀포",       33.1, 33.35, 126.1, 127.0, "11G00000", "11G00401", "서귀포"),

        // 강원 영동 (태백산맥 동쪽, 경도 128.5° 이상)
        Region("강원영동",     37.0, 38.6, 128.5, 129.4, "11D20000", "11D20501", "강릉"),

        // 강원 영서 (태백산맥 서쪽)
        Region("강원영서",     37.0, 38.6, 127.0, 128.5, "11D10000", "11D10301", "춘천"),

        // 경기 남부 (위도 36.9~37.4°, 경기 남부 수원 권역) — 서울보다 먼저 배치
        Region("경기남부",     36.9, 37.4, 126.7, 127.6, "11B00000", "11B20601", "수원"),

        // 서울·인천·경기 북부 (위도 37.4°N 이상)
        Region("서울/경기북부", 37.4, 38.3, 126.0, 127.9, "11B00000", "11B10001", "서울"),

        // 충남·대전·세종
        Region("충남/대전",    35.9, 37.2, 125.9, 127.5, "11C10000", "11C10301", "대전"),

        // 충북
        Region("충북",         36.0, 37.3, 127.3, 128.5, "11C20000", "11C20401", "청주"),

        // 전북·광주
        Region("전북/광주",    35.2, 36.3, 126.3, 128.0, "11H10000", "11H10501", "전주"),

        // 전남
        Region("전남",         34.0, 35.5, 125.8, 127.8, "11H20000", "11H20101", "목포"),

        // 경북·대구 (위도 35.5°N 이상, 경도 128°E 이상)
        Region("경북/대구",    35.4, 37.3, 127.8, 129.6, "11F20000", "11F20401", "대구"),

        // 경남·부산·울산
        Region("경남/부산",    34.5, 35.7, 127.5, 129.6, "11F10000", "11F10501", "부산")
    )

    /**
     * 위도/경도 → 중기육상예보 지역 코드
     */
    fun getLandRegionId(lat: Double, lon: Double): String =
        findRegion(lat, lon)?.landRegId ?: "11B00000" // 기본값: 서울·경기

    /**
     * 위도/경도 → 중기기온예보 지점 코드
     */
    fun getTaRegionId(lat: Double, lon: Double): String =
        findRegion(lat, lon)?.taRegId ?: "11B10001" // 기본값: 서울

    /**
     * 위도/경도 → 지역명 (디버깅·로그용)
     */
    fun getRegionName(lat: Double, lon: Double): String =
        findRegion(lat, lon)?.name ?: "알 수 없음"

    private fun findRegion(lat: Double, lon: Double): Region? =
        regions.firstOrNull { r ->
            lat in r.latMin..r.latMax && lon in r.lonMin..r.lonMax
        }
}
